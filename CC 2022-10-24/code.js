function accum(s) {
  // declare variable for empty string
  let result = ''
  // turn string into lower case and split
  let array = s.toLowerCase().split('')
  // for loop to turn index[0] into Upper case
  for (let i = 0; i < array.length; i++){
    result += array[i].toUpperCase()
    // 2nd for loop to decrease the number of letters per each character
    for (let j = i; j > 0; j--){
      result += array[i]
    }
    // if else in include the '-' in between each set of characters
    if (i < array.length - 1) {
      result += '-'
    }
  }
  return result
}

